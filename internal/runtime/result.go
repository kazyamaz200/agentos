// Package runtime manages the execution lifecycle of coding tasks, including
// planning, execution, testing, linting, review, and result generation.
package runtime

import (
	"time"
)

// Plan represents a structured execution plan generated by the planner.
type Plan struct {
	Summary             string  `json:"plan_summary"`
	Steps               []Step  `json:"steps"`
	EstimatedFilesChanged int   `json:"estimated_files_changed"`
}

// Step represents a single step within an execution plan.
type Step struct {
	StepNumber  int      `json:"step_number"`
	Action      string   `json:"action"`
	Description string   `json:"description"`
	TargetFiles []string `json:"target_files"`
	Reasoning   string   `json:"reasoning"`
}

// ExecutionResult holds the outcome of executing a plan, including per-step results and a diff.
type ExecutionResult struct {
	StepResults []StepResult `json:"step_results"`
	Diff        string       `json:"diff"`
	Success     bool         `json:"success"`
	Error       string       `json:"error,omitempty"`
}

// StepResult captures the outcome of a single plan step execution.
type StepResult struct {
	StepNumber int    `json:"step_number"`
	Action     string `json:"action"`
	Success    bool   `json:"success"`
	Output     string `json:"output"`
	Error      string `json:"error,omitempty"`
	Duration   time.Duration `json:"duration"`
}

// ReviewResult holds the output of a code review, including approval status and issues.
type ReviewResult struct {
	Approved bool          `json:"approved"`
	Issues   []ReviewIssue `json:"issues"`
	Summary  string        `json:"summary"`
}

// ReviewIssue describes a single issue found during code review.
type ReviewIssue struct {
	Severity string `json:"severity"`
	File     string `json:"file"`
	Line     int    `json:"line"`
	Message  string `json:"message"`
}
